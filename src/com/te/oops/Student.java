package com.te.oops;

public class Student {

	private String name;
	private int age;
	private double marks;
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public void setAge (int age)
	{
		this.age = age;
	}
	
	public void setMarks (double marks)
	{
		this.marks = marks;
	}
	
	public String getName() 
	{
		return name;
	}
	
	public int getAge() 
	{
		return age;
	}
	
	public double getMarks() 
	{
		return marks;
	}

}
